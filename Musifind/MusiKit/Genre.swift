import UIKit

/**
	Enum of available genres.

	- Rock: Rock genre.
	- Metal: Metal genre.
	- Pop: Pop genre.

	- None: Specifies "no genre"
*/
public enum Genre: String
{
	case Rock = "rock"
	case Metal = "metal"
	case Pop = "pop"
	
	case None = "none"
}